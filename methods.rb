# Puts Method => Always puts a line break after a Statement.
# Strings in Puts Methods
puts "Hello Ruby!"
puts "I made $10 dollars or a profit of 10%"
# Integars/Numbers in Puts Method
puts 5
puts 3.14159
puts 5 + 7
# Print Method => Does not puts a line break or not even a space after a Statement.
print "Hello"
print "Ruby!"
print 12
# P Method => Adds more detial additionally to analyze the output of given Statement.
# Strings in P Method
# Did u notice it shows double quotes as well!
p "I love Ruby"
p 72